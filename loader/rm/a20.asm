bits 16

;=============================================================================;
; a20_enable                                                                  ;
;    - Enable the A20 line for the CPU                                        ;
;=============================================================================;
a20_enable:
    cli

    call a20_wait
    mov al, 0xAD
    out 0x64, al

    call a20_wait
    mov al, 0xD0
    out 0x64, al

    call a20_wait_2
    in al, 0x60
    push eax

    call a20_wait
    mov al, 0xD1
    out 0x64, al

    call a20_wait
    pop eax
    or al, 2
    out 0x60, al

    call a20_wait
    mov al, 0xAE
    out 0x64, al

    call a20_wait
    sti
    ret

;=============================================================================;
; a20_wait                                                                    ;
;    - A20 wait output                                                        ;
;=============================================================================;
a20_wait:
    in al, 0x64
    test al, 2
    jnz a20_wait
    ret

;=============================================================================;
; a20_wait_2                                                                  ;
;    - A20 wait input                                                         ;
;=============================================================================;
a20_wait_2:
    in al, 0x64
    test al, 1
    jz a20_wait_2
    ret
    
