bits 32

;=============================================================================;
; loader32                                                                    ;
;    - Loader 32-bit entry point                                              ;
;=============================================================================;
loader32:
    mov ax, 0x10
    mov ds, ax
    mov ss, ax
    mov es, ax
    mov esp, 0x90000

    mov esi, 0xB8000
    mov word [esi], 0x0F44
    hlt
