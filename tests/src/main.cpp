#include "gtest/gtest.h"
#include "partlib/CRC32.hpp"

TEST(CRCTest, CRC32) {
    auto result = crc32(0, "123456789", 9);
    EXPECT_EQ(0xCBF43926, result);
}

int main(int argc, char** argv) {
    ::testing::InitGoogleTest(&argc, argv);
    return RUN_ALL_TESTS();
}
